/*******************************************************************************
*@author Xuanting Liu
*@brief 延时指定微秒
*******************************************************************************/
void wait_us(u8 time)
{
	TIM6->ARR = 42 * time - 20;
	TIM6->CNT = 0x0;
	TIM6->SR = 0x0;
	TIM6->CR1 |= 0x1;

	while((TIM6->CR1 & 0x1) == 1) ;
}


/*******************************************************************************
*@author Xuanting Liu
*@brief 延时指定time*100微秒
*******************************************************************************/
void wait_100us(u8 time)
{
	while(time > 0)
	{
		wait_us(100);
		time--;
	}
}

/*******************************************************************************
*@author Xuanting Liu
*@brief 延时指定毫秒
*******************************************************************************/
void wait_ms_with_dis_int(u8 time)
{
	u16 cnt;

	cnt = time * 4;
	while(cnt--)
	{
		wait_us(250);
	}
}

/*******************************************************************************
* @brief get one timer from system
*	@author Xuanting Liu
*******************************************************************************/
timer_t get_one_timer(u32 time)
{ 
	return get_sys_tick() + time;
}

/*******************************************************************************
* @brief 更新计数器变量
*	@author Xuanting Liu
*******************************************************************************/
void update_sys_timer(void)
{
	/* not need disable int, because the same int of ARM cortex-M3 cannot
	   be nested */
	sys_tick = sys_tick + 1;
}

/*******************************************************************************
* @brief Get the current system tick number
*	@author Xuanting Liu
*******************************************************************************/
u64 get_sys_tick(void)
{
	u64 tmp;

	DIS_INT();
	tmp = sys_tick;
	EN_INT();

	return tmp;
}

/*******************************************************************************
* @brief check if the timer is expired
*	@author Xuanting Liu
*******************************************************************************/
u8 check_timer(timer_t timer)
{
	u64 cur_time;

	cur_time = get_sys_tick();
	
	if(cur_time >= timer) return 1;
	else return 0;
}
